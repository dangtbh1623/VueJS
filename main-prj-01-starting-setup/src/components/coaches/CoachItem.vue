<template>
  <li>
    <h3>{{ fullname }}</h3>
    <h4>${{ hourlyRate }}/hour</h4>
    <div>
      <base-badge v-for="area in areas" :key="area" :title="area" :type="area"></base-badge>
    </div>
    <div class="actions">
      <base-button mode="outline" :to="coachDetailsLink" link>View detail</base-button>
      <base-button :to="coachContactLink" link>Contact</base-button>
    </div>
  </li>
</template>

<script>
export default {
  props: ['id', 'firstName', 'lastName', 'areas', 'hourlyRate'],
  computed: {
    fullname() {
      return this.firstName + ' ' + this.lastName;
    },
    coachContactLink() {
      return this.$route.path + '/' + this.id + '/contact';
    },
    coachDetailsLink() {
      return this.$route.path + '/' + this.id;
    },
  },
};
</script>

